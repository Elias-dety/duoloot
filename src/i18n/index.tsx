type Messages = (typeof messages)[LanguageCode];

interface LanguageContextValue {
  language: LanguageCode;
  messages: Messages;
  setLanguage: (language: LanguageCode) => void;
}

const LanguageContext = createContext<LanguageContextValue | null>(null);

const isLanguageCode = (value: string | null): value is LanguageCode =>
  languageOptions.some((option) => option.code === value);

const getInitialLanguage = (): LanguageCode => {
  if (typeof window === 'undefined') return defaultLanguage;

  const storedLanguage = window.localStorage.getItem(storageKey);
  return isLanguageCode(storedLanguage) ? storedLanguage : defaultLanguage;
};

export const getNextLanguage = (language: LanguageCode): LanguageCode => {
  const currentIndex = languageOptions.findIndex((option) => option.code === language);
  const nextIndex = (currentIndex + 1) % languageOptions.length;
  return languageOptions[nextIndex].code;
};

export const LanguageProvider: React.FC<React.PropsWithChildren> = ({ children }) => {
  const [language, setLanguageState] = useState<LanguageCode>(getInitialLanguage);

  const setLanguage = (nextLanguage: LanguageCode) => {
    setLanguageState(nextLanguage);
  };

  useEffect(() => {
    window.localStorage.setItem(storageKey, language);
    document.documentElement.lang = language;
  }, [language]);

  const value = useMemo<LanguageContextValue>(
    () => ({
      language,
      messages: messages[language],
      setLanguage,
    }),
    [language],
  );

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used inside LanguageProvider');
  }
  return context;
};
